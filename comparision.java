class comparision
{
	public static void main(String[] args) 
	{
		int a1=10,b1=20; 
		System.out.println(a1>b1);
		System.out.println(a1<b1);
		System.out.println(a1<=b1);
		System.out.println(a1>=b1);
		System.out.println(a1==b1);
		System.out.println(a1!=b1);
		System.out.println(10>20);
		
	}
}